//Assignment Operators
// Operator	Example	    Same As
// =	        x = y	    x = y
// +=	        x += y	    x = x + y
// -=	        x -= y	    x = x - y
// *=	        x *= y	    x = x * y
// /=	        x /= y	    x = x / y
// %=	        x %= y	    x = x % y
// **=	        x **= y	    x = x ** y


let x = 4
let y = 2
console.log(x=y) //Assigning y value to x
console.log(x+=5)
console.log(x-=2)
console.log(x*=2)
console.log(x/=2)
console.log(x%=2)
console.log(x**=2)