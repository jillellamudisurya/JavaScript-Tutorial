// JavaScript Comparison Operators
// Operator	Description
// ==	equal to
// ===	equal value and equal type
// !=	not equal
// !==	not equal value or not equal type
// >	greater than
// <	less than
// >=	greater than or equal to
// <=	less than or equal to
// ?	ternary operator

let x = 4
let y = 2
let z = '4'
console.log(x==y)
console.log(x==z)
console.log(x===z)
console.log(x!=y)
console.log(x!=z)
console.log(x!==z)
console.log(x>y)
console.log(x<y)
console.log(x>y?'big':'small')
